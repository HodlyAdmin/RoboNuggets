function slugify(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

export function normalizePlatformId(value) {
  return PLATFORM_ID_ALIASES.get(value) || slugify(value);
}

