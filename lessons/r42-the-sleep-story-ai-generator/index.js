#!/usr/bin/env node
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { existsSync, readFileSync, mkdirSync } from 'fs';
import { log } from '../../shared/logger.js';
import { saveManifest } from './manifest.js';
import { executeprocessInput } from './providers/chatterbox.js';
import { executegenerateImages } from './providers/seedream.js';
import { executeassembleVideo } from './providers/ffmpeg.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_DIR = join(__dirname, 'output');
const CONFIG_PATH = join(__dirname, 'config.example.json');

async function main() {
  log.step('R42 | R42 - The Sleep Story AI Generator Pipeline');

  if (!existsSync(CONFIG_PATH)) {
    log.error('Missing config.example.json. Please create one based on the example.');
    process.exit(1);
  }

  const config = JSON.parse(readFileSync(CONFIG_PATH, 'utf-8'));
  const runDir = join(OUTPUT_DIR, `${config.projectName}_${Date.now()}`);
  mkdirSync(runDir, { recursive: true });

  const runData = { outputDir: runDir, projectName: config.projectName };

  // Stage: Process Input
  runData.processInput = await executeprocessInput(config.processInputConfig, runData);

  // Stage: Create Images
  runData.generateImages = await executegenerateImages(config.generateImagesConfig, runData.processInput);

  // Stage: Create Video
  runData.assembleVideo = await executeassembleVideo(config.assembleVideoConfig, runData.generateImages);

  // Output State Tracking
  await saveManifest(runData);

  log.success(`\n🎉 Pipeline complete. Outputs saved in ${runDir}`);
}

main().catch(err => {
  log.error('Pipeline Failed: ' + err.message);
  process.exit(1);
});
